    private void jFormattedTextField_L_FindKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jFormattedTextField_L_FindKeyReleased
        // TODO add your handling code here:
        Connection conn = (Connection) connection.ConnectMySQL();
        tableModel = (DefaultTableModel) Table_LuongNV.getModel();
        ResultSet rs = null;
        String input = null;
        try {
            input = String.valueOf(jFormattedTextField_L_Find.getText());
            CallableStatement cStmt = (CallableStatement) conn.prepareCall("select soca.nv as 'manv', soca.ten as 'tennv', cv.ma_cv as 'cv',\n" +
                "					cv.hesoluong as 'dongia', soca.sc as 'soca', soca.sc*cv.hesoluong as 'luong'\n" +
                "		from chucvu cv, (select cl.ma_nv nv , nv.ma_cv cv, count(cl.ma_calam) sc, nv.ten_nv ten \n" +
                "						from calam cl, nhanvien nv\n" +
                "						where cl.ma_nv = nv.ma_nv and nv.ma_nv=?\n" +
                "						group by cl.ma_nv) soca\n" +
                "		where cv.ma_cv = soca.cv \n" +
                "		order by soca.nv;");
            cStmt.setString(1, input);
            rs = cStmt.executeQuery();
            tableModel.setRowCount(0);
            while (rs.next()) {
                String manv = rs.getString("manv");
                String tennv = rs.getString("tennv");
                String cv = rs.getString("cv");
                String dongia = rs.getString("dongia");
                String sc = rs.getString("soca");
                String luong = rs.getString("luong");
                tableModel.addRow(new String[]{manv, tennv, cv, dongia, sc, luong});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Không có thông tin nhân viên bạn cần tìm", "ERROR", 1);
        }
    }//GEN-LAST:event_jFormattedTextField_L_FindKeyReleased

